// Helper function to get random item from array
export const getRandomItem = <T,>(array: T[]): T => {
  return array[Math.floor(Math.random() * array.length)];
};

// Helper function to get random Malaysian name
export const getRandomMalaysianName = () => {
  const ethnicities = ['malay', 'chinese', 'indian'] as const;
  const ethnicity = getRandomItem(ethnicities);
  return getRandomItem(malaysianNames[ethnicity]);
};

// Helper function to generate random email from name
export const generateEmailFromName = (name: string): string => {
  const cleanName = name.toLowerCase()
    .replace(/\s+bin\s+/g, '.')
    .replace(/\s+binti\s+/g, '.')
    .replace(/\s+/g, '.')
    .replace(/[^a-z.]/g, '');
  return `${cleanName || 'user'}@edu.com`;
};
